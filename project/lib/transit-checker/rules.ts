export function isNationalityGroup(slug: string, group: NationalityGroup): boolean {
  switch (group) {
    case 'eu_eea': return EU_EEA_SLUGS.includes(slug);
    case 'five_eyes': return FIVE_EYES_SLUGS.includes(slug);
    case 'gulf_gcc': return GCC_SLUGS.includes(slug);
    case 'schengen_non_eu': return ['norway', 'switzerland', 'iceland', 'liechtenstein'].includes(slug);
    case 'any': return true;
  }
}

