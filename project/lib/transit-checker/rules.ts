export function findTransitRules(
  passportSlug: string,
  transitCountrySlug: string,
  airportCode: string,
): TransitRule[] {
  return TRANSIT_RULES.filter(rule => {
    if (rule.transit_country_slug !== transitCountrySlug) return false;
    if (rule.airport_codes.length > 0 && !rule.airport_codes.includes(airportCode)) return false;

    const matchesPassport = rule.passport_slugs?.includes(passportSlug);
    const matchesGroup = rule.nationality_groups?.some(g => isNationalityGroup(passportSlug, g));

    return matchesPassport || matchesGroup;
  });
}
